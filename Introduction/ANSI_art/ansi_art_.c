#include <stdio.h>

int main() {

    const char *art[] = {
        "        ████████      ████████        ",
        "     ████████████████████████████     ",
        "   ████████████████████████████████   ",
        "  ██████████████████████████████████  ",
        " ████████████████████████████████████ ",
        " ███████████  COLORFUL  █████████████ ",
        " ████████████████████████████████████ ",
        "  ██████████████████████████████████  ",
        "   ████████████████████████████████   ",
        "     ████████████████████████████     ",
        "        ████████      ████████        "
    };

    int rows = sizeof(art) / sizeof(art[0]);

    for (int y = 0; y < rows; y++) {
        for (int x = 0; art[y][x] != '\0'; x++) {

            int k = x + y * 4;  // diagonal color flow

            int r = (k * 7) % 255;
            int g = (k * 3) % 255;
            int b = (k * 11) % 255;

            printf("\033[38;2;%d;%d;%dm%c", r, g, b, art[y][x]);
        }
        printf("\n");
    }

    printf("\033[0m");
    return 0;
}
